package com.jobatda.back.domain.user.entity;

/**
 * 사용자 권한을 정의하는 열거형 클래스
 * 
 * 권한별 접근 제어 규칙:
 * - ADMIN (관리자): 모든 게시판에 대한 읽기/쓰기 권한, 공지사항 작성 가능
 * - COMPANY (회사원): 기업게시판과 일반 게시판 접근 가능, 공지사항은 읽기만 가능
 * - GENERAL (일반 유저): 자유게시판과 Q&A만 접근 가능, 공지사항은 읽기만 가능, 기업게시판 접근 불가
 */
public enum UserRole {
    /**
     * 관리자 권한
     * - 모든 게시판 읽기/쓰기 가능
     * - 공지사항 작성 가능
     * - 사용자 관리 권한 보유
     */
    ADMIN,
    
    /**
     * 회사원 권한  
     * - 기업게시판 읽기/쓰기 가능
     * - 자유게시판, Q&A 읽기/쓰기 가능
     * - 공지사항 읽기만 가능
     */
    COMPANY,
    
    /**
     * 일반 사용자 권한
     * - 자유게시판, Q&A 읽기/쓰기 가능
     * - 공지사항 읽기만 가능  
     * - 기업게시판 접근 불가
     */
    GENERAL
}